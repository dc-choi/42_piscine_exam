#include <unistd.h>

int main()
{
	char b = 'z';
	char a = 'Y';
	while (a >= 'A')
	{
		write(1, &b, 1);
		write(1, &a, 1);
		b -= 2;
		a -= 2;
	}
	write(1, "\n", 1);
	return 0;
}
