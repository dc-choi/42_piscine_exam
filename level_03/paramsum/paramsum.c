#include <unistd.h>

void ft_putchar(char c)
{
	write(1, &c, 1);
}

void ft_putnbr(int nbr)
{
	if (nbr > 9)
	{
		ft_putnbr(nbr / 10);
		ft_putnbr(nbr % 10);
	}
	else
		ft_putchar(nbr + '0');
}

int main(int argc, char **argv)
{
	(void)argv;
	argc--;
	ft_putnbr(argc);
	ft_putchar('\n');
	return 0;
}
